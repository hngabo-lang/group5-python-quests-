#!/usr/bin/env python3
#  You need 3 dragon scales (10 gold each) and 5 elf tears (3 gold each). Calculate the total cost
dragon_scales = 3
elf_tears = 5
dragon_scale_cost = 10
elf_tear_cost = 3

total_cost = (dragon_scales * dragon_scale_cost) + (elf_tears * elf_tear_cost)
print(f"The total cost is {total_cost} gold.")